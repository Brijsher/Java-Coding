
public class Array_Example_1 
{
	public static void main(String[] args) 
	{
		//Declaration and Instantiation
		int a[]=new int[5];
		//Initialization
		a[0]=10;
		a[1]=20;
		a[2]=70;
		a[3]=40;
		a[4]=50;
		//Array Traversing
		for(int i=0; i<a.length; i++)  //Length is the Property of the Array
			System.out.println(a[i]);
				
	}

}
