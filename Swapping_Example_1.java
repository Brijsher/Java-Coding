// JAVA Program to SWAP two Numbers
// WAY-1
public class Swapping_Example_1 
{
	public static void main(String[] args) 
	{
		int a=5;
		int b=4;
		int temp;
		System.out.println("Before SWAPPING: a = "+a + " and b = "+b);
		
		temp=a;    // temp=5
		a=b;      //  a=4
		b=temp;  //   b=5
		System.out.println("After SWAPPING: a = "+a + " and b = "+b);
	}
}
